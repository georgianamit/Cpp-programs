#include<iostream>

using namespace std;

int main(){
    // prints hello world on separate lines
    cout << "hello" << endl << "world";
    cout << endl ;
    cout << "hello\nworld";
    cout << endl;
    
    // prints hello world on the same line
    cout << "hello world";    
    cout << endl;
    cout << "hello " << "world";  
}